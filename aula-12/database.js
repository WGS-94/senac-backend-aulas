/*
    Banco de Dados: Coleção organizada de infos ou dados, que podem ser acessados, 
    gerenciados, atualizados e manipulados.

    Ex: Agenda de contatos de celulares (armazenamos números, nome, etc)

    Caracteristicas de um Banco de Dados:

    - Organização
    - Gerenciamento
    - Acessibilidade (acesso)
    - Manipulação (inserção, alteração, exclusão)
    - Escabilidade (ampliação)
    - Segurança (autenticação, autorização, etc)
    - Persistência (armazenamento)

    Tipos de Bando de Dados:

    - Relacional (MySQL, PostgreSQL)
    - Não Relacional (MongoDB, Cassandra, NoSQL, Neo4)
*/